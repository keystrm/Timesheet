    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required',
            'description'=>'required',
            'created_by'=>'required'
        ]);
        try {
            $project=Project::create($request->all());

            if($project){
                return redirect()->back()->with('success', 'Project Has Been inserted');
            }
            else{
                return redirect()->back()->with('failed', 'Project creation is failed');
            }
        }catch(Throwable $e){
            return redirect()->back()->with('failed', 'Project creation is failed'.$e);
        }
    }

